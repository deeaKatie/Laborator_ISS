package model;

public class Project implements Entity<Integer> {
    private Integer id;
    private String name;

    public Project() {
        this.id = -1;
        this.name = "";
    }
    public Project(String name) {
        this.name = name;
    }

    @Override
    public Integer getId() {
        return id;
    }

    @Override
    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Project{" +
                "name='" + name + '\'' +
                '}';
    }
}
